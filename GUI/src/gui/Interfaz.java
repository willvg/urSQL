    private String[] QueryToArray(String Query){ // toma la entrada del area de texto y genera arreglo
        String linea="";
        int indice=0;
        for (int i=0; i<Query.length();i++){
            if (Query.charAt(i)=='\n'){
                indice=indice+ this.QueryToArrayAux(linea).length;
                linea="";
            }
            else{
                linea= linea+Query.charAt(i);
                if (i+1==Query.length() && linea.length()!=0){
                    indice=indice+ this.QueryToArrayAux(linea).length;
                    linea="";
                }
            }
        }
        String out[]= new String[indice];
        indice=0;
        for (int i=0; i<Query.length();i++){
            if (Query.charAt(i)=='\n'){
                String [] aux= this.QueryToArrayAux(linea);
                for (int j=0; j<aux.length; j++){
                    out[indice]= aux[j];
                    indice++;
                }
                linea="";
            }
            else{
                linea= linea+Query.charAt(i);
                if (i+1==Query.length() && linea.length()!=0){
                    String [] aux= this.QueryToArrayAux(linea);
                    for (int j=0; j<aux.length; j++){
                          out[indice]= aux[j];
                          indice++;
                    }
                    linea="";
                }
            }
        }
        return out;
    }
    
    private String[] QueryToArrayAux(String linea){
        int indice=0;
        String palabra="";
        for (int i=0; i<linea.length(); i++){
            if (linea.charAt(i)==' '){
                indice++;
                palabra="";
            }
            else{
                palabra= palabra+linea.charAt(i);
                if (i+1==linea.length() && palabra.length()!=0){
                    indice++;
                    palabra="";
                }
            }
        }
        String out[]= new String [indice];
        indice=0;
        palabra="";
        for (int i=0; i<linea.length(); i++){
            if (linea.charAt(i)==' '){
                out[indice]=palabra;
                indice++;
                palabra="";
            }
            else{
                palabra= palabra+linea.charAt(i);
                if (i+1==linea.length() && palabra.length()!=0){
                    out[indice]=palabra;
                    indice++;
                    palabra="";
                }
            }
        }
        return out;
    }
    
