    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String text= this.jTextArea1.getText();
        String []QueryArray= this.QueryToArray(text);
        String Query= "";
        for (int i=0; i<QueryArray.length;i++){
            Query=Query+"/"+QueryArray[i];
        }
        Query= Query+"/";
        String TipoConsulta= conexion.SendReceive("T,"+Query);
        if (TipoConsulta.length()>3){
            this.jTextArea2.setText("Error, no puede haber mas de un tipo de comando en la consulta!");
        }
        else if (TipoConsulta.length()<3){
            this.jTextArea2.setText("Error, no se encontro ningun comando valido!");
        }
        else if (TipoConsulta.equals("CLP") && !esAdmi){
            this.jTextArea2.setText("Usted no posee permisos para ejecutar comandos CLP!");
        }
        else{
            String respuesta= conexion.SendReceive(Query);
            this.jTextArea2.setText(respuesta);
        }
        
    }//GEN-LAST:event_jButton5ActionPerformed

