    private boolean Conectar(String host, String port){ //funcion que usa el api para conectarse a la bd
        int p=0;
        try{
            p= Integer.parseInt(port);
        }
        catch (Exception e){
            return false;
        }
        conexion= new ApiDBMS2(host, p);
        String respuesta= conexion.SendReceive("Prueba");
        if (!respuesta.equals("Connected")){
            System.out.println(respuesta);
            return false;
        }
        else{
            return true;
        }
    }
    
    private boolean existeUsuario(String usuario, String contrasena){ //funcion que verifica la exst. de usuario y si es admi.
        String respuesta= conexion.SendReceive("C,"+usuario+","+contrasena);
        if (respuesta.equals("Administrador")){
            esAdmi=true;
            return true;
        }
        else if (respuesta.equals("Usuario")){
            esAdmi=false;
            return true;
        }
        else{
            return false;
        }
    }
    
